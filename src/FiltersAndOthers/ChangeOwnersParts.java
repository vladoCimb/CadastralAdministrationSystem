    private void inicialization() {

        jPanel1.setLayout(new GridLayout(10, 1));
        JTextField[] fields = new JTextField[oldParts.size()];
        JLabel[] labels = new JLabel[oldParts.size()];
        ArrayList<String> returnArray = new ArrayList();

        for (int i = 0; i < oldParts.size(); i++) {
            String[] parts = oldParts.get(i).split(";");
            // returnArray.add(parts[0] + ";" + test1[i].getText());    
           
            fields[i] = new JTextField() ;
            labels[i] = new JLabel("Rodné číslo: " + parts[0] + "--- Meno a Priezvisko: " + system.findPersonByID(Long.parseLong(parts[0])).getFirstName() + " " + system.findPersonByID(Long.parseLong(parts[0])).getSurnName());
            
        
            jPanel1.add(labels[i]);
            jPanel1.add(fields[i]);
            textFields.add(fields[i]);
            jLabes.add(labels[i]);
            
        }
        

    }
    

