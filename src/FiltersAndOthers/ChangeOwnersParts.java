    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double sum = 0.0;
        ArrayList<String> newOwnersParts = new ArrayList();
        for(int i = 0; i < textFields.size();i++){
            sum+= Double.parseDouble(textFields.get(i).getText());
            String[] parts = oldParts.get(i).split(";");
            newOwnersParts.add(parts[0] + ";" + textFields.get(i).getText());
        }
        if(sum != 1.0){
            JOptionPane.showMessageDialog(Globals.panel, "Súčet všetkých vlastníckych podielov musí byť rovný 1!");
        } else {
            system.findPropertyListByNumber(Integer.parseInt(propertyListNumb)).getOwnership().setPersonParts(newOwnersParts);
            list.showList();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

