    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (regNumbTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Zadajte číslo");
            return;
        } else {
            String parts = regNumbTF.getText();
            if (system.findPropertyListByNumber(Integer.parseInt(parts)).getCadastralArea() != null) {
                JOptionPane.showMessageDialog(this, "Zadaný list vlastníctva už obsahuje katastrálne územie.");
                return;
            } else {
                system.findCadArea(Integer.parseInt(cadAreaID)).addPropertyList(system.findPropertyListByNumber(Integer.parseInt(parts)));
                system.findPropertyListByNumber(Integer.parseInt(parts)).setCadastralArea(system.findCadArea(Integer.parseInt(cadAreaID)));
            }
        }
        area.showList();
    }//GEN-LAST:event_jButton1ActionPerformed

