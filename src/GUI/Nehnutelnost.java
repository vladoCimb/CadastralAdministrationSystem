    private void adddBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adddBTActionPerformed
        if (regNumbTF.getText().isEmpty() || adressTF.getText().isEmpty() || descriptionTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(mainWindow, "Vyplňte súpisné číslo, adresu a popis k nehnuteľnosti");
            return;
        } else {
            system.addProperty(Integer.parseInt(regNumbTF.getText()), adressTF.getText(), descriptionTF.getText());
            regNumbTF.setText("");
            adressTF.setText("");
            descriptionTF.setText("");
        }

    }//GEN-LAST:event_adddBTActionPerformed

