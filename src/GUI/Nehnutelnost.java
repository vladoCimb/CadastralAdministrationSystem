    private void changeOwnerBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeOwnerBTActionPerformed
        int row = tableOwnersTB.getSelectedRow();
        double oldOnwership = 0.0;
        int indexOfDelete = -5;
        if (row >= 0) {
            String deleteID = tableOwnersTB.getModel().getValueAt(row, 2).toString();
            ArrayList<String> list = system.findPropertyListByNumber(Integer.parseInt(propertyListTF.getText())).getOwnership().getPersonParts();
            for (int i = 0; i < list.size(); i++) {
                String[] values = list.get(i).split(";");
                if (values[0].compareTo(deleteID) == 0) {
                    indexOfDelete = i;
                    oldOnwership = Double.parseDouble(values[1]);
                }
            }
            ///dorobit
            if (system.findPropertyListByNumber(Integer.parseInt(propertyListTF.getText())).getOwnership().getPersons().insert(system.findPersonByID(Long.parseLong(newOwnersIDTF.getText())))) {

                system.findPropertyListByNumber(Integer.parseInt(propertyListTF.getText())).getOwnership().getPersonParts().add(system.findPersonByID(Long.parseLong(newOwnersIDTF.getText())).getIdentifNumb() + ";" + Double.toString(oldOnwership));
                if (indexOfDelete >= 0) {
                    system.findPropertyListByNumber(Integer.parseInt(propertyListTF.getText())).getOwnership().getPersons().delete(system.findPersonByID(Long.parseLong(deleteID)));
                    system.findPropertyListByNumber(Integer.parseInt(propertyListTF.getText())).getOwnership().getPersonParts().remove(indexOfDelete);
                    showList();
                }
            } else {
                JOptionPane.showMessageDialog(mainWindow, "Nový majiteľ s daným rodným číslom neexistuje");
            }

        } else {
            JOptionPane.showMessageDialog(mainWindow, "Vyberte majiteľa, ktorého chcete zmeniť.");
        }

    }//GEN-LAST:event_changeOwnerBTActionPerformed


